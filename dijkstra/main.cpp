void Maze::DecorateRoom(unsigned char* TileMap) {
	LoadedMap = TileMap;
	unsigned short sum = 0;
	for (int y = 0; y < 24; y++) {
		for (int x = 0; x < 32; x++) {
			pencil->DrawBGColour(x, y, LoadedMap[sum]);
			sum++;
		}
	}
}


